package entire;

public class department {
	private int id;					/*部门编号*/
	private String dep;				/*部门名*/
	private String des;				/*简介*/
	/*部门编号*/
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id=id;
	}//
	/*部门名*/
	public String getDep() {
		return dep;
	}
	public void setDep(String dep) {
		this.dep=dep;
	}//
	/*简介*/
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des=des;
	}//
}
